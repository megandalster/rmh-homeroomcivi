<p> <strong>How to Search for a Booking in the Database</strong>

<p> <B>Step 1:</B> On the navigation bar at the top of the page, find <B>bookings:</B> and select <B>search</B>, like this:<BR><BR>
	<a href="tutorial/screenshots/searchBookingStep1.png" class="image" title="searchBookingStep1.png"
					target="tutorial/screenshots/searchBookingStep1.png">
					&nbsp;&nbsp;&nbsp;&nbsp;<img src="tutorial/screenshots/searchBookingStep1.png" width="75%" border="1px" align="middle"></a></p>
<p> <B>Step 2:</B> You can enter the primary guest's first name, last name, or any of the other listed values as a search criterion.
				   After typing your criterion in the appropriate box/boxes, select the <B>Search</B> button, like this:<BR><BR>
	<a href="tutorial/screenshots/searchBookingStep2.png" class="image" title="searchBookingStep2.png"
					target="tutorial/screenshots/searchBookingStep2.png">
					&nbsp;&nbsp;&nbsp;&nbsp;<img src="tutorial/screenshots/searchBookingStep2.png" width="75%" border="1px" align="middle"></a></p>
<p> <B>Step 3:</B> Now you will see a list of bookings in the database that match your search criterion, like this:<BR><BR>
				  <a href="tutorial/screenshots/searchBookingStep3.png" class="image" title="searchBookingStep3.png"
				  	target="tutorial/screenshots/searchBookingStep3.png">
				 	&nbsp;&nbsp;&nbsp;&nbsp;<img src="tutorial/screenshots/searchBookingStep3.png" width="75%" border="1px"  align="middle"></a></p>
				  <p> If this list has the name of the booking you want to view, then select <B>view</B>
									 to the right of that booking.  <br>
<p> <B>Step 4:</B> If you don't see what you were looking for, you can try again.  The
									search box always reappears at the top of the page above the results, like this:<BR><BR>
				  <a href="tutorial/screenshots/searchBookingStep4.png" class="image" title="searchBookingStep4.png"
					target="tutorial/screenshots/searchBookingStep4.png">
