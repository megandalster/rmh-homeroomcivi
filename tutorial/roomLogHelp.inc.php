
	<p> <strong>How to Work with Room Logs</strong></p>
	<p> 
		<B>Step 1:</B> On the navigation bar at the top of the page, locate
		<b>room logs</b> like so:<br /> <a href="tutorial/screenshots/roomLogHelp1.png"
		class="image" title="roomLogHelp1.png"><img src="tutorial/screenshots/roomLogHelp1.png"
		width="75%" border="1px"></a>
	</p>

	<p> 
		<B>Step 2:</B> You'll see a screen that looks like the following:<br />
		<a href="tutorial/screenshots/roomLogHelp2.png" class="image" 
		title="roomLogHelp2.png"><img src="tutorial/screenshots/roomLogHelp2.png"
		width="75%" border="1px"></a><br />
		
		This is the main room log view. From here you will be able to view and modify the
		status of the rooms.
	</p>
	<p> 
		<B>Step 3:</B> Each section is labeled with a number in this image:<br /> 
		<a href="tutorial/screenshots/roomLogHelp3.png" class="image" 
		title="roomLogHelp3.png"><img src="tutorial/screenshots/roomLogHelp3.png"
		width="75%" border="1px"></a><br />
		A brief explanation for each section is as follows:
		<ol>
			<li>The warning message is only displayed if a house manager has not yet
			saved the day's room log. If the room log is not saved, no room log will
			be stored for that date.</li>
			<li>The current date of the room log being viewed. By default, clicking on the
			room log link on the navigation bar takes you to today's room log.</li>
			<li>The navigation bar gives you two choices in viewing a room log: you may 
			navigate day-by-day or enter a custom date. To enter a custom date, no
			fields can be left blank.</li>
			<li>The room log. Here you can see the status of the 21 rooms. You can edit 
			rooms by clicking on them.</li>
			<li>A list of all pending bookings. Here you can view a list of all pending bookings
			or select an individual booking for viewing.</li>
			<li>A button to allow saving/updating the current room log.</li>
		</ol>
	</p>
	<p>
		<b>Step 4:</b> View any pending bookings. At the bottom of the screen will be a
		drop-down list of all pending bookings:<br /> <a href="tutorial/screenshots/roomLogHelp6.png"
		class="image" title="roomLogHelp6.png"><img src="tutorial/screenshots/roomLogHelp6.png"
		width="50%" border="1px"></a><br />
		You may select one and then click "View Booking", which will open a new tab that displays
		information about that booking:<br />
		<a href="tutorial/screenshots/roomLogHelp7.png" class="image" title="roomLogHelp7.png">
		<img src="tutorial/screenshots/roomLogHelp7.png" width="60%" border="1px"></a><br />
		Note that if there are no pending bookings, there will be no button to view a booking.
	</p>
	<p>
		<b>Step 5:</b> Select a room to edit. From the room log, you can view information about
		all of the rooms, such as status, number of beds, and information about the guests 
		staying there. The primary guest will be in bold. Start by hovering your mouse over a room. 
		The room will highlight grey like so:<br />
		<a href="tutorial/screenshots/roomLogHelp4.png" class="image" title="roomLogHelp4.png">
		<img src="tutorial/screenshots/roomLogHelp4.png" width="40%" border="1px"></a><br />
	</p>
	<p>
		<b>Step 6:</b> Click the room you want to edit. This will open a new page that looks 
		like the following:<br /> <a href="tutorial/screenshots/roomLogHelp5.png" class="image"
		title="roomLogHelp5.png"><img src="tutorial/screenshots/roomLogHelp5.png"
		width="50%" border="1px"></a><br />
		From here, you can edit information about the room and assign bookings from a list
		of all pending bookings. If the room is booked, the latter option will be replaced
		by the option to check-out the family. Once finished, click the "submit" button. You will be
		redirected to the room log page.
	</p>
	
	<p> <b>Step 7:</b> You can return to any other function by selecting it on the navigation bar.</p>
	
	<p> <strong>Working with Day Use</strong></p>
	
	<p> <b>Step 1:</b> 
	
		At the bottom of the page, you will see a "Day Use" section. The number of boxes shown will be equal to 
		the number of pending bookings with day use. Refer to "How to Create a Booking" for more information on 
		creating a day use booking. To check in or check out a day use guest simply click an empty day use box.
	</p>
	
